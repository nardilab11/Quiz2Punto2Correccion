package modelo;

/**
 *
 * @author Estudiante
 */
public class Video {
    protected String titulo;
    protected String director;
    protected int annio;

    public Video(String titulo, String director, int annio) {
        this.titulo = titulo;
        this.director = director;
        this.annio = annio;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getDirector() {
        return director;
    }

    public void setDirector(String director) {
        this.director = director;
    }

    public int getAnnio() {
        return annio;
    }

    public void setAnnio(int annio) {
        this.annio = annio;
    }
    
    
}
